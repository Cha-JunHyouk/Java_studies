package study_200814;

public class ArithOp 
{

	public static void main(String[] args) 
	{
		int num1=7;
		int num2=3;
		
		System.out.println("num1+num2="+(num1+num2));
		System.out.println("num1-num2="+(num1-num2));
		System.out.println("num1*num2="+(num1*num2));
		System.out.println("num1/num2="+(num1/num2)); //실수로 표현할 경우 나머지가 존재하지 않는 실수형 나눗셈의 값으로 출력
		System.out.println("num1%num2="+(num1%num2));

	}

}
